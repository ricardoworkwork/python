fast_food = ["mac", "Burguer king", "chiplote", "wendys"]
print(fast_food)
# sorted() faz sort temporario | fast_food.sort() é permanente
print(sorted(fast_food))
print(fast_food)

# fast_food.sort(reverse=True) organiza ao contrario por ordem alfabetica
# fast_food.reverse() só inverte a lista
fast_food.reverse()
print(fast_food)

# lenght -> len() function
print(len(fast_food))
